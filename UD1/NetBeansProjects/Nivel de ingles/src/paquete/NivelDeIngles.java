    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int notaWriting = jSlider1Writing.getValue();
        int notaSpeaking = jSlider1Speaking.getValue();
        int notaReading = jSlider1Reading.getValue();
        int notaListenening = jSlider1Listening.getValue();
        int media = ((notaWriting + notaSpeaking + notaReading + notaListenening) / 4);
      
        if (B1.isSelected()) {
            if (media<120) {
                notaFinal.setText(String.valueOf("No se obtiene certificado. Media: " + media));
             }else if (media >= 120 && media<= 139){
                 notaFinal.setText(String.valueOf("A2. Media: " + media));
             }else if (media>= 140 && media<= 159) {
                notaFinal.setText(String.valueOf("B1. Media: " + media));
            }else notaFinal.setText(String.valueOf("B2. Media: " + media));
        }else{
              if (media<140) {
                notaFinal.setText(String.valueOf("No se obtiene certificado. Media: " + media));
             }else if (media >= 140 && media<= 159){
                 notaFinal.setText(String.valueOf("B1. Media: " + media));
             }else if (media>= 160 && media<= 179) {
                notaFinal.setText(String.valueOf("B2. Media: " + media));
            }else notaFinal.setText(String.valueOf("C1. Media: " + media));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

