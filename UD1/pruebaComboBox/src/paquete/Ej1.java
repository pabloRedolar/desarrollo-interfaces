    public void crearCombos(){
        comboNivel.removeAllItems();
        comboNota.removeAllItems();
        
        comboNivel.addItem("A1");
        comboNivel.addItem("A2");
        comboNivel.addItem("B1");
        comboNivel.addItem("B2");
        comboNivel.addItem("C1");
        comboNivel.addItem("C2");
        comboNivel.setSelectedIndex(-1);

        for (int i = 5; i < 10+1; i++) {
            comboNota.addItem(String.valueOf(i));
        }
        comboNota.setSelectedIndex(-1);
    }
